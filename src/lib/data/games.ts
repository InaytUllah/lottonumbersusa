// Get all games for a state (including national games)
export function getGamesForState(stateSlug: string): GameConfig[] {
  return [...NATIONAL_GAMES, ...(STATE_GAMES[stateSlug] || [])];
}

// Get a specific game by slug
export function getGameBySlug(gameSlug: string, stateSlug?: string): GameConfig | undefined {
  const national = NATIONAL_GAMES.find(g => g.slug === gameSlug);
  if (national) return national;
  if (stateSlug && STATE_GAMES[stateSlug]) {
    return STATE_GAMES[stateSlug].find(g => g.slug === gameSlug);
  }
  // Search all states
  for (const state of Object.values(STATE_GAMES)) {
    const found = state.find(g => g.slug === gameSlug);
    if (found) return found;
  }
  return undefined;
}

// Get all unique game slugs for sitemap
export function getAllGameSlugs(): { stateSlug?: string; gameSlug: string }[] {
  const slugs: { stateSlug?: string; gameSlug: string }[] = [];
  NATIONAL_GAMES.forEach(g => slugs.push({ gameSlug: g.slug }));
  Object.entries(STATE_GAMES).forEach(([stateSlug, games]) => {
    games.forEach(g => slugs.push({ stateSlug, gameSlug: g.slug }));
  });
  return slugs;
}
